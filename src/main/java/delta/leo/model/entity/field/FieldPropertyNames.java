package delta.leo.model.entity.field;

/**
 * Standard field properties names.
 * @author DAM
 */
public class FieldPropertyNames
{
  /**
   * "MANDATORY" field property.<p>
   * A mandatory field must be assigned a value.
   */
  public static final String MANDATORY="MANDATORY";
  /**
   * "ID" field property.<p>
   * An ID field is used to uniquely identify an entity.
   */
  public static final String ID="ID";
  /**
   * "NAME" field property.<p>
   * A NAME field is used to build a human-readable short string for an entity.
   */
  public static final String NAME="NAME";
}
